WITH CTE_RANKING AS (
    SELECT ID, RANK() OVER (ORDER BY SIZE_OF_COLONY) AS RANK_VALUE 
    FROM ECOLI_DATA
)
SELECT ID,
CASE WHEN RANK_VALUE <= (SELECT COUNT(ID) FROM ECOLI_DATA) /4
THEN 'LOW'
WHEN RANK_VALUE > (SELECT COUNT(ID) FROM ECOLI_DATA) / 4
AND RANK_VALUE <= (SELECT COUNT(ID) FROM ECOLI_DATA) / 2
THEN 'MEDIUM'
WHEN RANK_VALUE > (SELECT COUNT(ID) FROM ECOLI_DATA) / 2 
AND RANK_VALUE <= 3*(SELECT COUNT(ID) FROM ECOLI_DATA) / 4
THEN 'HIGH'
ELSE 'CRITICAL'
END AS COLONY_NAME
FROM CTE_RANKING
ORDER BY ID;
