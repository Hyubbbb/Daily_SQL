-- GROUP BY: 식품분류별 가장 비싼 식품의 정보 조회하기
-- https://school.programmers.co.kr/learn/courses/30/lessons/131116

-- 1.
SELECT FP.CATEGORY, FP.PRICE AS MAX_PRICE, FP.PRODUCT_NAME
FROM FOOD_PRODUCT FP
JOIN (
    SELECT CATEGORY, MAX(PRICE) AS MAX_PRICE
    FROM FOOD_PRODUCT
    WHERE CATEGORY IN ('과자', '국', '김치', '식용유')
    GROUP BY CATEGORY
) MAX_FP ON FP.CATEGORY = MAX_FP.CATEGORY AND FP.PRICE = MAX_FP.MAX_PRICE
ORDER BY FP.PRICE DESC;

-- 2.
SELECT CATEGORY, PRICE AS MAX_PRICE, PRODUCT_NAME
FROM FOOD_PRODUCT
WHERE CATEGORY IN ('과자', '국', '김치', '식용유')
AND PRICE = (SELECT MAX(PRICE) FROM FOOD_PRODUCT FP WHERE FP.CATEGORY = FOOD_PRODUCT.CATEGORY)
ORDER BY PRICE DESC;
