SELECT 
    H.HISTORY_ID,
    FLOOR(C.DAILY_FEE * (DATEDIFF(H.END_DATE, H.START_DATE) + 1) 
          * (1 - IFNULL(P.DISCOUNT_RATE, 0) / 100)) AS FEE
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY H
JOIN CAR_RENTAL_COMPANY_CAR C
    ON H.CAR_ID = C.CAR_ID
LEFT JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN P
    ON C.CAR_TYPE = P.CAR_TYPE
   AND (
        (DATEDIFF(H.END_DATE, H.START_DATE) + 1 >= 90  AND P.DURATION_TYPE = '90일 이상')
     OR (DATEDIFF(H.END_DATE, H.START_DATE) + 1 >= 30  AND DATEDIFF(H.END_DATE, H.START_DATE) + 1 < 90  AND P.DURATION_TYPE = '30일 이상')
     OR (DATEDIFF(H.END_DATE, H.START_DATE) + 1 >= 7   AND DATEDIFF(H.END_DATE, H.START_DATE) + 1 < 30  AND P.DURATION_TYPE = '7일 이상')
   )
WHERE C.CAR_TYPE = '트럭'
ORDER BY FEE DESC, H.HISTORY_ID DESC;
